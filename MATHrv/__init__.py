def addition(a, b):
	return a+b

def add1(num):
	return num+1

def tri_area(base, height):
	return (base*height)/2

def next_edge(side1, side2):
	return (side1 + side2)-1

def remainder(x, y):
	return x%y

def string_int(txt):
	return int(txt)

def find_perimeter(length, width):
	return (length+width)*2

def sum_polygon(n):
	return (n-2)*180

def calculate_exponent(num, exp):
	return num**exp

def squared(b):
	return b*b

def to_int(txt):
	return int(txt)

def to_str(num):
	return str(num)

def is_seven(x):
	return x==7
